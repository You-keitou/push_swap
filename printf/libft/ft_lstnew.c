#include "libft.h"

t_list	*ft_lstnew(int *content)
{
	t_list	*newnode;

	newnode = (t_list *)malloc(sizeof(t_list));
	if (newnode == NULL)
		return (NULL);
	newnode->content = content;
	newnode->next = newnode;
	newnode->prev = newnode;
	return (newnode);
}
